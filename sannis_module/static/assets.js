/**
 * Populates a given select element with categorized asset data.
 * @param {string} selectElementId - The ID of the <select> element to populate.
 */
function populateSymbolDropdown(selectElementId) {
    const selectEl = document.getElementById(selectElementId);
    if (!selectEl) {
        console.error(`Element with ID "${selectElementId}" not found.`);
        return;
    }

    selectEl.innerHTML = '<option value="" disabled>Select an Asset</option>';

    for (const category in ASSET_DATA) {
        const optgroup = document.createElement('optgroup');
        optgroup.label = `--- ${category} ---`;

        ASSET_DATA[category].forEach(asset => {
            const option = document.createElement('option');
            option.value = asset.symbol;
            // Display a cleaner text in the dropdown
            option.textContent = `${asset.symbol} (${asset.description})`;

            optgroup.appendChild(option);
        });

        selectEl.appendChild(optgroup);
    }
}